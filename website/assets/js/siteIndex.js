// 修正网站索引中的URL
function fixSiteIndexUrls() {
    // 修正中文索引URL
    siteIndexData.zh.forEach(page => {
        if (page.url.includes('/')) {
            const urlParts = page.url.split('/');
            page.url = './' + urlParts[urlParts.length - 1];
        } else if (!page.url.startsWith('./') && !page.url.startsWith('../') && !page.url.startsWith('http')) {
            page.url = './' + page.url;
        }
    });
    
    // 修正英文索引URL
    siteIndexData.en.forEach(page => {
        if (page.url.includes('/')) {
            const urlParts = page.url.split('/');
            page.url = './' + urlParts[urlParts.length - 1];
        } else if (!page.url.startsWith('./') && !page.url.startsWith('../') && !page.url.startsWith('http')) {
            page.url = './' + page.url;
        }
    });
}

// 初始化时修正URL
fixSiteIndexUrls();

// 根据当前语言获取网站索引
function getSiteIndex() {
    const lang = localStorage.getItem('language') || 'zh';
    return siteIndexData[lang] || siteIndexData['zh']; // 如果找不到当前语言的索引，默认使用中文
}

// 导出网站索引
let siteIndex = getSiteIndex();

// 监听语言变化事件
document.addEventListener('languageChanged', function(e) {
    // 更新网站索引
    siteIndex = getSiteIndex(); // 完全替换为新语言的索引数据
}); 